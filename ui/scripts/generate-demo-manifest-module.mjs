import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const uiRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const manifestRoot = path.join(uiRoot, 'public', 'demos', 'manifests');
const outputPath = path.join(uiRoot, 'src', 'generated', 'demoManifests.generated.js');
const index = JSON.parse(fs.readFileSync(path.join(manifestRoot, 'index.json'), 'utf8'));
const manifests = Object.fromEntries(index.releases.map((release) => {
  const manifest = JSON.parse(fs.readFileSync(path.join(manifestRoot, `${release.stack_version}.json`), 'utf8'));
  if (manifest.stack_version !== release.stack_version || manifest.mip_version !== release.mip_version) {
    throw new Error(`Demo manifest index mismatch for ${release.stack_version}`);
  }
  return [release.stack_version, manifest];
}));

const generated = `// Generated by scripts/generate-demo-manifest-module.mjs. Do not edit.\nexport const DEMO_INDEX = ${JSON.stringify(index, null, 2)};\nexport const DEMO_MANIFESTS = ${JSON.stringify(manifests, null, 2)};\n`;

if (process.argv.includes('--check')) {
  if (!fs.existsSync(outputPath) || fs.readFileSync(outputPath, 'utf8') !== generated) {
    throw new Error('Generated demo manifest module is stale. Run the generator and commit its output.');
  }
} else {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, generated, 'utf8');
}
